"""
Programação Orientada à Objetos - POO

- POO é um paradigma de programação que utiliza mapeamento
de objetos do mundo real para modelos computacionais.

- Paradigma de programação é a forma/metodologia utilizada
para pensar/desenvolver sistemas.

Principais elementos da POO:
- Classe -> Modelo do objeto do mundo real sendo representado computacionalmente
- Atributo -> Características do objeto.
- Método -> Comportamento do objeto (funções).
- Construtor -> Método especial utilizado para criar objetos.
- Objeto -> Instância da classe.
"""

print('Variável:')
numero = 10
print(numero)
print(type(numero))
print()

class Produto:
    pass


# Produto() -> Construtor
ps4 = Produto()
print('Classe:')
print(ps4)
